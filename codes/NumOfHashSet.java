import java.util.HashSet;

public class NumOfHashSet {
    public static void main(String[] args) {
        HashSet <String> set = new HashSet<>();
        set.add("White");
        set.add("Black");
        set.add("Blue");
        set.add("Red");
        set.add("Green");
        set.add("Pink");
        System.out.println("Total Number of Elemets are: "+ set.size());
    }
}
